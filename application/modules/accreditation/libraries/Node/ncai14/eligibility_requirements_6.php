namespace Node\ncai14;

/**
 * Description of eligibility_requirements_6
 *
 * @author laith
 */
class Eligibility_Requirements_6 extends \Orm_Node
{

    protected $class_type = __CLASS__;
    protected $name = '6. Student Handbooks and guides';
    protected $link_view = true;
    protected $link_edit = true;
    protected $link_pdf = true;

    public function init()
    {
        parent::init();

            $this->set_info();
            $this->set_attachment('');
            $this->set_comment('');
    }

    public function set_info()
    {
        $property = new \Orm_Property_Fixedtext('info', ' The institution must have published guides (or catalogues or handbooks) that are readily accessible to existing and potential future students, and teaching and other staff, that include accurate and current information about details of programs and courses, degrees offered and graduation requirements, admission requirements and procedures, costs and refund policies (if applicable), rules and regulations directly affecting students.');
        $this->set_property($property);
    }

    public function get_info()
    {
        return $this->get_property('info')->get_value();
    }

    public function set_attachment($value)
    {
        $property = new \Orm_Property_Upload('attachment', $value);
        $property->set_description('Provide a copy of student handbooks, guides, and prospectus');
        $this->set_property($property);
    }

    public function get_attachment()
    {
        return $this->get_property('attachment')->get_value();
    }

    public function set_comment($value)
    {
        $property = new \Orm_Property_Textarea('comment', $value);
        $property->set_description('Comment');
        $this->set_property($property);
    }

    public function get_comment()
    {
        return $this->get_property('comment')->get_value();
    }

}
