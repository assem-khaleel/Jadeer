namespace Node\ncai14;

/**
 * Description of eligibility_requirements_18
 *
 * @author laith
 */
class Eligibility_Requirements_18 extends \Orm_Node
{

    protected $class_type = __CLASS__;
    protected $name = '18. Alumni or Graduate Data';
    protected $link_view = true;
    protected $link_edit = true;
    protected $link_pdf = true;

    public function init()
    {
        parent::init();

            $this->set_info();
            $this->set_attachment('');
            $this->set_comment('');
    }

    public function set_info()
    {
        $property = new \Orm_Property_Fixedtext('info', 'A new or recently established institution must have been in operation long enough for its first cohort of students to have graduated and information from its graduates about the quality of their programs must be included in evidence provided for accreditation.');
        $this->set_property($property);
    }

    public function get_info()
    {
        return $this->get_property('info')->get_value();
    }

    public function set_attachment($value)
    {
        $property = new \Orm_Property_Upload('attachment', $value);
        $property->set_description('Provide a copy of the alumni guidebook or graduation data');
        $this->set_property($property);
    }

    public function get_attachment()
    {
        return $this->get_property('attachment')->get_value();
    }

    public function set_comment($value)
    {
        $property = new \Orm_Property_Textarea('comment', $value);
        $property->set_description('Comment');
        $this->set_property($property);
    }

    public function get_comment()
    {
        return $this->get_property('comment')->get_value();
    }

}
