namespace Node\ncassr18;

/**
 * Description of eligibility_requirements_5
 *
 * @author laith
 */
class Eligibility_Requirements_5 extends \Orm_Node
{

    protected $class_type = __CLASS__;
    protected $name = '5. Program or Course Requirements ';
    protected $link_view = true;
    protected $link_edit = true;
    protected $link_pdf = true;

    public function init()
    {
        parent::init();

            $this->set_info();
            $this->set_attachment('');
            $this->set_comment('');
    }

    public function set_info()
    {
        $property = new \Orm_Property_Fixedtext('info', ' Clearly stated descriptions must be available of course content, program requirements, and other regulations affecting students in the program, including institution or collegeâ€“wide requirements as well as those specific to the program concerned.');
        $this->set_property($property);
    }

    public function get_info()
    {
        return $this->get_property('info')->get_value();
    }

    public function set_attachment($value)
    {
        $property = new \Orm_Property_Upload('attachment', $value);
        $property->set_description('Insert in the box (or provide a link) copies of the descriptions of program and course requirements or regulations.s');
        $this->set_property($property);
    }

    public function get_attachment()
    {
        return $this->get_property('attachment')->get_value();
    }

    public function set_comment($value)
    {
        $property = new \Orm_Property_Textarea('comment', $value);
        $property->set_description('Comment');
        $this->set_property($property);
    }

    public function get_comment()
    {
        return $this->get_property('comment')->get_value();
    }

}
