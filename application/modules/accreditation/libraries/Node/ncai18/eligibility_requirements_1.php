namespace Node\ncai18;

/**
 * Description of Eligibility_Requirements_1
 *
 * @author laith
 */
class Eligibility_Requirements_1 extends \Orm_Node
{

    protected $class_type = __CLASS__;
    protected $name = '1. Final Licence';
    protected $link_view = true;
    protected $link_edit = true;
    protected $link_pdf = true;

    public function init()
    {
        parent::init();

            $this->set_info();
            $this->set_attachment('');
            $this->set_comment('');
    }

    public function set_info()
    {
        $property = new \Orm_Property_Fixedtext('info', 'The institution must be established by the Government of Saudi Arabia as a higher education institution, or if a private institution, have been granted a final license to operate as a higher education institution in Saudi Arabia by the Ministry of Education or other government authority authorized by the Higher Council of Education.');
        $this->set_property($property);
    }

    public function get_info()
    {
        return $this->get_property('info')->get_value();
    }

    public function set_attachment($value)
    {
        $property = new \Orm_Property_Upload('attachment', $value);
        $property->set_description('Attached a copy of the final licence by approved government institution');
        $this->set_property($property);
    }

    public function get_attachment()
    {
        return $this->get_property('attachment')->get_value();
    }

    public function set_comment($value)
    {
        $property = new \Orm_Property_Textarea('comment', $value);
        $property->set_description('Comment');
        $this->set_property($property);
    }

    public function get_comment()
    {
        return $this->get_property('comment')->get_value();
    }

}
